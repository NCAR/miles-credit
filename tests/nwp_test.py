def test_build_GFS_init():
    date = pd.Timestamp("2025-01-05")
    base_path, dir_path = generate_synthetic_gfs_data(date)
    output_grid = xr.Dataset(
        coords={"latitude": np.arange(-88.5, 90, 2), "longitude": np.arange(0, 360, 2)},
    )
    model_levels = np.array(
        [
            1.0,
            9.0,
            19.0,
            29.0,
            39.0,
            49.0,
            59.0,
            69.0,
            79.0,
            89.0,
            97.0,
            104.0,
            111.0,
            116.0,
            122.0,
            126.0,
            131.0,
            136.0,
        ]
    )
    variables = ["u_component_of_wind", "v_component_of_wind", "temperature", "specific_total_water"]
    gfs_regridded_data = build_GFS_init(
        output_grid,
        date,
        variables,
        model_level_file="ERA5_Lev_Info.nc",
        model_levels=model_levels,
        gdas_base_path=base_path,
        variable_mapping="arcoera5",
    )
    assert gfs_regridded_data.dims == {
        "latitude": output_grid["latitude"].size,
        "longitude": output_grid["longitude"].size,
        "level": model_levels.size,
        "time": 1,
    }
    gfs_path = join(base_path, dir_path.split("/")[0])
    cleanup_gfs_data(gfs_path)
    return
